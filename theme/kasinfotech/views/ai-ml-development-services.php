                        </div>
                        <div class="accordian-text" itemscope="" itemprop="acceptedAnswer" itemtype="https://schema.org/Answer" style="display: none;">
                            <ul itemprop="text">
                                <li>Unavailability of a large number of training samples.</li>
                                <li>Labelling of Data - As deep learning and conventional machine learning algorithms are supervised, i.e., they need data and their label to capture the semantics of work to be done. It is a manual process and eats up more time than actually building models. It also adds biases in data as humans are prone to error when it comes to accurately annotating data, e.g. Annotating car parts for detecting damage. Model build with such data generally doesn't converse with reasonable accuracy.</li>
                                <li>Adopting Generality - Ml/DL algorithms are not able to produce the same results when deployed to different scenarios than the scenario used while training. So, to make it work in a different situation, a retraining model is required.</li>
